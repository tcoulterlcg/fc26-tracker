// Reconcile teamcrafters index (team|conference) with the exact reference team
// names, then emit lib/cfbConferences.js as { conference: [team, ...] }.
import { readFileSync, writeFileSync } from 'node:fs'
import { createClient } from '@supabase/supabase-js'

let envText = readFileSync(new URL('../.env.local', import.meta.url), 'utf8')
envText = envText.replace(/^﻿/, '')
const env = {}
for (const line of envText.split(/\r?\n/)) {
  const m = line.replace(/^﻿/, '').trim().match(/^([A-Za-z0-9_]+)\s*=\s*(.*)$/)
  if (m) env[m[1]] = m[2].trim()
}
const sb = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.SUPABASE_SERVICE_ROLE_KEY, { auth: { persistSession: false } })

// distinct reference teams
let refTeams = new Set()
{
  let from = 0
  while (true) {
    const { data } = await sb.from('cfb_player_reference').select('team').range(from, from + 999)
    for (const r of data) refTeams.add(r.team)
    if (data.length < 1000) break
    from += 1000
  }
}
const refList = [...refTeams]

const pairs = readFileSync(new URL('../scripts/data/cfb/cfb_conferences.txt', import.meta.url), 'utf8')
  .split(/\r?\n/).filter(Boolean).map(l => l.split('|'))

const norm = s => s.toLowerCase().replace(/[^a-z0-9]/g, '')
function resolveRef(indexName) {
  if (refTeams.has(indexName)) return indexName
  // e.g. index "Miami" -> reference "Miami (FL)"
  const paren = refList.find(t => t.startsWith(indexName + ' ('))
  if (paren) return paren
  // normalized match
  const nn = norm(indexName)
  const nm = refList.find(t => norm(t) === nn)
  return nm || null
}

const byConf = {}
const unmatched = []
for (const [name, conf] of pairs) {
  const ref = resolveRef(name)
  if (!ref) { unmatched.push(name); continue }
  ;(byConf[conf] = byConf[conf] || []).push(ref)
}
for (const c of Object.keys(byConf)) byConf[c].sort()

console.log('conferences:', Object.keys(byConf).map(c => `${c}(${byConf[c].length})`).join(', '))
console.log('total teams mapped:', Object.values(byConf).reduce((a, b) => a + b.length, 0))
if (unmatched.length) console.log('UNMATCHED (index name not in reference):', JSON.stringify(unmatched))

const js = `// Auto-generated by scripts/gen-conferences.mjs from the teamcrafters CFB 27
// index. Maps each conference to the schools in it, using the exact team names
// as stored in cfb_player_reference (so a dropdown selection imports cleanly).
export const CFB_CONFERENCES = ${JSON.stringify(byConf, null, 2)}

export const CFB_CONFERENCE_NAMES = ${JSON.stringify(Object.keys(byConf).sort())}
`
writeFileSync(new URL('../lib/cfbConferences.js', import.meta.url), js)
console.log('wrote lib/cfbConferences.js')
