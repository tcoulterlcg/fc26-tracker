// Generate lib/cfbLogos.js: our cfb_player_reference team names -> ESPN logo
// URLs (transparent 500px PNGs from a.espncdn.com). Match by ESPN "location"
// with manual overrides for ambiguous names. Input: espn_cfb.json
// (curl "https://site.api.espn.com/apis/site/v2/sports/football/college-football/teams?limit=500")
import { readFileSync, writeFileSync } from 'node:fs'
import { createClient } from '@supabase/supabase-js'

let envText = readFileSync(new URL('../.env.local', import.meta.url), 'utf8')
envText = envText.replace(/^﻿/, '')
const env = {}
for (const line of envText.split(/\r?\n/)) {
  const m = line.replace(/^﻿/, '').trim().match(/^([A-Za-z0-9_]+)\s*=\s*(.*)$/)
  if (m) env[m[1]] = m[2].trim()
}
const sb = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.SUPABASE_SERVICE_ROLE_KEY, { auth: { persistSession: false } })

const espn = JSON.parse(readFileSync(new URL('../espn_cfb.json', import.meta.url), 'utf8'))
  .sports[0].leagues[0].teams.map(t => t.team)

// our team name -> ESPN displayName, for names that don't match by location
const OVERRIDES = {
  'Miami (FL)': 'Miami Hurricanes',
  'Miami (OH)': "Miami (OH) RedHawks",
  'Northwestern': 'Northwestern Wildcats',
  'Ole Miss': 'Ole Miss Rebels',
  'NC State': 'NC State Wolfpack',
  'UCF': 'UCF Knights',
  'USC': 'USC Trojans',
  'SMU': 'SMU Mustangs',
  'TCU': 'TCU Horned Frogs',
  'BYU': 'BYU Cougars',
  'LSU': 'LSU Tigers',
  'UAB': 'UAB Blazers',
  'UTSA': 'UTSA Roadrunners',
  'UTEP': 'UTEP Miners',
  'FIU': 'Florida International Panthers',
  'UMass': 'UMass Minutemen',
  'UNLV': 'UNLV Rebels',
  'UConn': 'UConn Huskies',
  'Hawaii': "Hawai'i Rainbow Warriors",
  'San Jose State': 'San José State Spartans',
  'Louisiana': 'Louisiana Ragin’ Cajuns',
  'Appalachian State': 'App State Mountaineers',
  'Sacramento State University': 'Sacramento State Hornets',
  'UL–Monroe': 'UL Monroe Warhawks',
}

// distinct team names from the reference
let teams = new Set(), from = 0
while (true) {
  const { data } = await sb.from('cfb_player_reference').select('team').range(from, from + 999)
  for (const r of data) teams.add(r.team)
  if (data.length < 1000) break
  from += 1000
}

const map = {}
const unmatched = []
for (const name of [...teams].sort()) {
  let hit = null
  if (OVERRIDES[name]) {
    hit = espn.find(t => t.displayName === OVERRIDES[name]) ||
          espn.find(t => t.displayName.toLowerCase().includes(OVERRIDES[name].toLowerCase().split(' ')[0]) && /hurricanes|redhawks/i.test(t.displayName))
  }
  if (!hit) {
    const locMatches = espn.filter(t => t.location === name)
    if (locMatches.length === 1) hit = locMatches[0]
    else if (locMatches.length > 1) hit = locMatches.find(t => t.logos && t.logos.length) || locMatches[0]
  }
  if (!hit) hit = espn.find(t => t.displayName.startsWith(name + ' '))
  const logo = hit && hit.logos && hit.logos[0] && hit.logos[0].href
  if (logo) map[name] = logo
  else unmatched.push(name)
}

console.log('mapped:', Object.keys(map).length, 'of', teams.size)
if (unmatched.length) console.log('UNMATCHED:', JSON.stringify(unmatched))

const js = `// Auto-generated by scripts/gen-cfb-logos.mjs — official ESPN team logos
// (transparent 500px PNGs) keyed by cfb_player_reference team name.
export const CFB_LOGOS = ${JSON.stringify(map, null, 2)}
`
writeFileSync(new URL('../lib/cfbLogos.js', import.meta.url), js)
console.log('wrote lib/cfbLogos.js')
